class Product {
  final String name;
  final String imageUrl;
  final double price;
  final String description;

  Product({required this.name, required this.imageUrl, required this.price, required this.description});
}

class Restaurant {
  final String name;
  final String description;
  final String imageUrl;
  final double rating;

  Restaurant({required this.name, required this.description, required this.imageUrl, required this.rating});
} 